# review_tags.py
# Этот файл определяет пользовательские теги и фильтры для шаблонов Django,
# предназначенные для работы с отзывами и связанными данными (например, словарями).
# Основная цель — упростить доступ к данным в шаблонах.

# Импортируем модуль template из Django, который нужен для работы с шаблонами.
from django import template

# Создаём объект библиотеки шаблонов, который будет регистрировать наши фильтры и теги.
register = template.Library()

# Регистрируем фильтр get_item с помощью декоратора @register.filter.
# Этот фильтр позволяет получать значение из словаря по указанному ключу.
@register.filter
def get_item(dictionary, key):
    """
    Извлекает значение из словаря по заданному ключу.

    Args:
        dictionary: Словарь (dict), из которого нужно получить значение.
        key: Ключ (строка или другой тип), по которому ищется значение.

    Returns:
        Значение, соответствующее ключу, или None, если ключ не найден.

    Example:
        В шаблоне: {{ my_dict|get_item:'key_name' }} → вернёт значение my_dict['key_name']
        Если ключа нет, вернёт None.
    """
    return dictionary.get(key)  # Используем метод get(), который возвращает None при отсутствии ключа.